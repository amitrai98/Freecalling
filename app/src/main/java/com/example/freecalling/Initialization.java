package com.example.freecalling;

public class Initialization {

	String name,no,url;
	public Initialization(String name,String no,String url)
	{
		this.name=name;
		this.no=no;
		this.url=url;
	}
	public String getName()
	{
		return name;
	}
	
	public String getNo()
	{
		return no;
	}
	public String getUrl()
	{
		return url;
	}
}
